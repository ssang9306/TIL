-- 10 SELECT 
SELECT  O.ORDERDATE, C.CNAME,O.ADDRESS
        , O.PHONE, O.STATUS, P.PNAME,
        OT.COST, OT.QTY, OT.COST*OT.QTY
FROM    ORDERS  O
JOIN    ORDERDETAIL OT ON (O.ORDERNO = OT.ORDERNO)
JOIN    CUSTOMERS C ON (O.CNO = C.CNO)
JOIN    PRODUCTS P ON (P.PNO = OT.PNO);

-- 11 일별 매출 목록

SELECT  ORDERDATE,
        SUM(COST *QTY)
FROM    ORDERS
JOIN    ORDERDETAIL USING(ORDERNO)
GROUP BY ORDERDATE
ORDER BY 1;

